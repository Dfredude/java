package helpers;

public class PrintLn {
	public PrintLn() {
		
	}
	
	public PrintLn(String text) {
		println(text);
	}
	
	public PrintLn(int text) {
		println(text);
	}
	
	public PrintLn(boolean text) {
		println(text);
	}
	
	
	public void println(String text) {
		System.out.println(text);
	}
	
	public void println(int text) {
		System.out.println(text);
	}
	
	public void println(boolean text) {
		System.out.println(text);
	}
}
