package Chapter06;

public class dReadInput {
	public static void main(String numbers[]) {
		//4 strings are taken as "numbers" arguments
		float num_1 = Float.parseFloat(numbers[0]);
		float num_2 = Float.parseFloat(numbers[1]);
		float num_3 = Float.parseFloat(numbers[2]);
		float num_4 = Float.parseFloat(numbers[3]);
		System.out.println("I like these numbers: ");
		System.out.println(num_1);
		System.out.println(num_2);
		System.out.println(num_3);
		System.out.println(num_4);
	}

}
